var numero = 1;
//en los parametros del switch se puede poner el numero ya
switch (numero) {
    case 1:
        console.log("soy uno");
        break;
    case 10:
        console.log("soy un diez");
        break;
    case 100:
        console.log("soy un cien");
        break;
    default:
        console.log("no soy ninguno");
}


